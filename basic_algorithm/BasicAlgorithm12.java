package basic_algorithm;

public class BasicAlgorithm12 {
	
	/*	
	 * 다중 루프!!
	 * 제일제일 초초초초초초기초 문제
	 * 곱셈식을 만드시오.
	 * 구구다안~
	 * 
	 */
	public static void main(String[] args) {
		
		System.out.println("곱셈표 시이이작");
		
		for(int i=1; i<=9;i++) {
			for(int j=1; j<=9; j++) {
				System.out.printf("%3d",i*j);
			}
			System.out.println();
		}
		System.out.println("곱셈표 끄으으읏!!! 진짜쉽죠잉");
	}
	


}
